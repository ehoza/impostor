            // Create impostor word
            $impostor = Word::create([
                'word' => $impostorWord,
                'category' => $category,
                'definition' => null,
                'is_impostor_word' => true,
                'difficulty' => rand(1, 3),
                'language' => $language,
            ]);

            // Link crew word to impostor word
            $crew->update(['impostor_word_id' => $impostor->id]);

            $wordMap[$crewWord] = $crew;
            $wordMap[$impostorWord] = $impostor;
        }

        $this->command->info('Seeded '.count($wordMap).' words ('.(count($wordMap) / 2).' pairs) across English and Romanian categories.');
    }
