    /**
     * Run the database seeds.
     */
    public function run(): void
    {
        DB::transaction(function (): void {
            Word::query()->delete();

            $now = now();
            $allWords = [];

            foreach ($this->wordPairs as $crewWord => $impostorWord) {
                $crewWord = trim($crewWord);
                $impostorWord = trim($impostorWord);

                $allWords[$crewWord] = [
                    'word' => $crewWord,
                    'category' => $this->categorizeWord($crewWord),
                    'is_impostor_word' => false,
                    'difficulty' => rand(1, 3),
                ];
                $allWords[$impostorWord] = [
                    'word' => $impostorWord,
                    'category' => $this->categorizeWord($impostorWord),
                    'is_impostor_word' => true,
                    'difficulty' => rand(1, 3),
                ];
            }

            foreach (array_chunk(array_values($allWords), 100) as $chunk) {
                $toInsert = array_map(fn ($w) => array_merge($w, [
                    'definition' => null,
                    'impostor_word_id' => null,
                    'created_at' => $now,
                    'updated_at' => $now,
                ]), $chunk);
                Word::insert($toInsert);
            }

            $wordIds = Word::pluck('id', 'word');

            foreach ($this->wordPairs as $crewWord => $impostorWord) {
                $crewWord = trim($crewWord);
                $impostorWord = trim($impostorWord);
                $crewId = $wordIds[$crewWord] ?? null;
                $impostorId = $wordIds[$impostorWord] ?? null;

                if ($crewId && $impostorId) {
                    Word::where('id', $crewId)->update(['impostor_word_id' => $impostorId]);
                }
            }
        });

        $this->command->info('Seeded ' . count($this->wordPairs) . ' word pairs.');
    }

    private function categorizeWord(string $word): string
    {
        $word = trim($word);
        $romanianPatterns = ['ă', 'â', 'î', 'ș', 'ț', 'Măr', 'Pâine', 'Branză', 'Mămăligă', 'Sarmale', 'Ciorbă', 'Țuică', 'Palincă', 'Mici', 'Cozonac', 'Zacuscă', 'Slănină', 'Câine', 'Pisică', 'Munte', 'Casă', 'Salut', 'Noroc', 'Tare', 'Mișto', 'Șmecher', 'Frate', 'Cluj', 'Brașov', 'Carpați', 'Dunăre', 'Transilvania', 'Maramureș', 'Bucovina', 'Dobrogea'];

        foreach ($romanianPatterns as $pattern) {
            if (stripos($word, $pattern) !== false) {
                return 'Romanian';
            }
        }

        return 'English';
    }
